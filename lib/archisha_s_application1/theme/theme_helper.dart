        ),
        displaySmall: TextStyle(
          color: appTheme.gray100,
          fontSize: 36.fSize,
          fontFamily: 'Dongle',
          fontWeight: FontWeight.w700,
        ),
        headlineLarge: TextStyle(
          color: appTheme.whiteA700,
          fontSize: 32.fSize,
          fontFamily: 'Dongle',
          fontWeight: FontWeight.w300,
        ),
        headlineMedium: TextStyle(
          color: appTheme.whiteA700,
